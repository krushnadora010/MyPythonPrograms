#using union function
x={10,20,30,40,50}
y={40,50,60,70,80,90}
print(x.union(y))
print(x|y)

# using intersection function

x={10,20,30,40,50}
y={40,50,60,70,80,90}
print(x.intersection(y))
print(x&y)

#using difference function

x={10,20,30,40,50}
y={40,50,60,70,80,90}
print(x.difference(y))
print(y.difference(x))

# using symmetric_difference function

x={10,20,30,40,50}
y={40,50,60,70,80,90}
print(x.symmetric_difference(y))






